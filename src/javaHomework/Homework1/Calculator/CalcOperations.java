package javaHomework.Homework1.Calculator;

public class CalcOperations {

    public double summary (double x, double y){
        double summ = x + y;
        return summ;
    }

    public double subtract (double x, double y){
        double subtr = x - y;
        return subtr;
    }

    public double multiplic (double x, double y){
        double mult = x * y;
        return mult;
    }

    public double division (double x, double y){
        double divis = x / y;
        return divis;
    }

}
